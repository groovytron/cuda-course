    bool isOk = true;

    int nbFleches = INT_MAX;
    float m = 20;
    float result;
    int mp = Device::getMPCount();

    dim3 dg = dim3(512, 1, 1);
    dim3 db = dim3(512, 1, 1);

    Grid grid(dg, db);

    MonteCarlo mc(grid, nbFleches, m);
    mc.run();
    result = mc.getResult();
    printf("result : %f", result);
    return isOk;
